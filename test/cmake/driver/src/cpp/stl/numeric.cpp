// https://en.cppreference.com/w/cpp/numeric/random/uniform_int_distribution#Example
//
namespace uniform_int_distribution_test {
void run() {
  // The cppreference example uses std::random_device to seed the engine.
  // Kernel tests use a fixed seed to avoid depending on hosted entropy APIs.
  std::mt19937 gen{};
  std::uniform_int_distribution<> distrib(1, 6);

  // Use distrib to transform the random unsigned int generated by gen into an
  // int in [1, 6].
  for (int n = 0; n != 10; ++n) {
    std::cout << distrib(gen) << ' ';
  }
  std::cout << '\n';
}
} // namespace uniform_int_distribution_test
