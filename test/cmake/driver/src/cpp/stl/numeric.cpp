namespace iterator_advance_test {
void run() {
  std::vector<int> v{3, 1, 4};

  auto vi = v.begin();
  std::advance(vi, 2);
  std::cout << *vi << ' ';

  vi = v.end();
  std::advance(vi, -2);
  std::cout << *vi << '\n';
}
} // namespace iterator_advance_test

